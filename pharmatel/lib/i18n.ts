export type TranslationKey = keyof typeof translations.en;

export function normalizeLanguage(value?: string | null): Language {
  return value?.toLowerCase().startsWith("ar") ? "ar" : "en";
}

export function detectPreferredLanguage(): Language {
  const locale =
    typeof Intl !== "undefined" && typeof Intl.DateTimeFormat === "function"
      ? Intl.DateTimeFormat().resolvedOptions().locale
      : typeof navigator !== "undefined"
        ? navigator.language
        : "en";
  return normalizeLanguage(locale);
}

export function getLocaleForLanguage(language: Language): string {
  return language === "ar" ? "ar-EG" : "en-US";
}

export function isRTL(language: Language): boolean {
  return language === "ar";
}

export function translate(
  language: Language,
  key: TranslationKey,
  params?: TranslationParams,
): string {
  const value = translations[language][key] ?? translations.en[key] ?? key;
  if (!params) return value;

  return Object.entries(params).reduce(
    (text, [paramKey, paramValue]) =>
      text.replaceAll(`{${paramKey}}`, String(paramValue)),
    value,
  );
}
